<?php
// Include config file
require_once 'config.php';

// Define variables and initialize with empty values
$username = $password = $confirm_password = "";
$username_err = $password_err = $confirm_password_err = "";

// Processing form data when form is submitted
if($_SERVER["REQUEST_METHOD"] == "POST"){
	//Custom made function to be used to check string for valid email addresses.
	function str_ends($haystack, $needle)
	{
		return 0 === substr_compare($haystack, $needle, -strlen($needle));
	}

    // Validate username
    if(empty(trim($_POST["username"]))){
        $username_err = "Please enter a valid email address.";
    } 
    elseif(strlen(trim($_POST['username'])) < 15) {
        $username_err = "Email address must have at least 15 characters.";
    }
    elseif(!strpos($_POST['username'], '@')) {
	    //We want to have email address. Possibly for the future, we can send confirmation links. Email addresses help prevent verbose message return and help ensure unique usernames.
        $username_err = "Username not permitted. Must use valid email address.";
    }
    elseif(!(str_ends($_POST['username'], '.com') || str_ends($_POST['username'], '.org') || str_ends($_POST['username'], '.edu'))) {
	    //This is basic error checking. We want a .com/.edu/.org email address. The string should end it those. (n.b. We used created function listed at top for this.)
        $username_err = "Username not permitted. Must use valid email address.";
    }
    else{
        // Prepare a select statement
        $sql = "SELECT id FROM users WHERE username = ?";

        if($stmt = mysqli_prepare($link, $sql)){
            // Bind variables to the prepared statement as parameters
            mysqli_stmt_bind_param($stmt, "s", $param_username);

            // Set parameters
            $param_username = trim($_POST["username"]);

            // Attempt to execute the prepared statement
            if(mysqli_stmt_execute($stmt)){
                /* store result */
                mysqli_stmt_store_result($stmt);

                if(mysqli_stmt_num_rows($stmt) == 1){
                    $username_err = "This email address is already taken.";
                } else{
                    $username = trim($_POST["username"]);
                }
            } else{
                echo "Oops! Something went wrong. Please try again later.";
            }
        }

        // Close statement
        mysqli_stmt_close($stmt);
    }

    // Validate password
    if(empty(trim($_POST['password']))){
        $password_err = "Please enter a password.";
    } elseif(strlen(trim($_POST['password'])) < 6){
        $password_err = "Password must have atleast 6 characters.";
    } else{
        $password = trim($_POST['password']);
    }

    // Validate confirm password
    if(empty(trim($_POST["confirm_password"]))){
        $confirm_password_err = 'Please confirm password.';
    } else{
        $confirm_password = trim($_POST['confirm_password']);
        if($password != $confirm_password){
            $confirm_password_err = 'Password did not match.';
        }
    }

    // Check input errors before inserting in database
    if(empty($username_err) && empty($password_err) && empty($confirm_password_err)){

        // Prepare an insert statement
        $sql = "INSERT INTO users (username, password) VALUES (?, ?)";

        if($stmt = mysqli_prepare($link, $sql)){
            // Bind variables to the prepared statement as parameters
            mysqli_stmt_bind_param($stmt, "ss", $param_username, $param_password);

            // Set parameters
            $param_username = $username;
            //$param_password = $password; We want to Modify this because we want to stored HASHED values of the password. It is never good to store the password itself. Not sure how this HASH function works, but ideally, we want to SALT our Hashes because people use the same password. The same password will yield the same HASH value. So we usually want to add a SALT, informational entropy, to make sure that even if users use the same password, the HASH values will still be different.
            $param_password = password_hash($password, PASSWORD_DEFAULT); 

            // Attempt to execute the prepared statement
            if(mysqli_stmt_execute($stmt)){
                // Redirect to login page
                header("location: login.php");
            } else{
                echo "Something went wrong. Please try again later.";
            }
        }

        // Close statement
        mysqli_stmt_close($stmt);
    }

    // Close connection
    mysqli_close($link);
}
